// 정수값을 메모리에 저장하는 원리 
package ch02;

public class Test04 {
  public static void main(String[] args) {
   // 5 - 5 의 결과를 출력하라
   System.out.println(5 - 5); //연산자와 피연산자 사이 공백 주기
    // 5 + (-5) 의 결과를 출력하라
    System.out.println(5 + (-5));
    
  }
}

